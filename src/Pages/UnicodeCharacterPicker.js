export default class UnicodeCharacterPicker extends Component {
  constructor() {
    super();
    this.state = {
      copiedCharacter: null
    }
  }

  render() {
    const style = {
      fontSize: '2rem'
    }

    const characterToComponent = (categoryId, character) => {
      const id = `character-${categoryId}-${character}`;
      return (
        <CopyToClipboard
          key={id}
          text={character}
          onCopy={() => this.onCopy(id)}
        >
          <span
            id={id}
            className={`unicode-characters-character${id === this.state.copiedCharacter ? ' unicode-characters-character--copied' : ''}`}
            style={style}
          >
            {character}
          </span>
        </CopyToClipboard>
      );
    }

    const charactersMapComponents = charactersMap.map(({ name, characters }) => {
      const categoryId = name.replace(/\s+/, '-').toLowerCase();
      return (
        <div key={categoryId}>
          <h2>
            <Typography use="title">{name}</Typography>
          </h2>
          <div>
            {Array.from(characters).map(c => characterToComponent(categoryId, c))}
          </div>
        </div>
      )
    });

    return (<div>{charactersMapComponents}</div>);
  }

  onCopy(id) {
    this.setState({ copiedCharacter: id });
  }
}
